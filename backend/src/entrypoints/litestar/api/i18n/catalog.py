def get_i18n_messages(language: LanguageEnum) -> LanguageMessages:
    return MESSAGES[language]


def get_language_label(language: LanguageEnum) -> str:
    labels: Mapping[LanguageEnum, str] = {
        LanguageEnum.RU: "Русский",
        LanguageEnum.EN: "English",
    }
    return labels[language]


def validate_i18n_catalog() -> None:
    baseline = set(MESSAGES[LanguageEnum.RU])
    for language in LanguageEnum:
        keys = set(MESSAGES[language])
        if keys != baseline:
            msg = f"I18n catalog for {language.value} does not match ru keys"
            raise RuntimeError(msg)

    for language in LanguageEnum:
        messages = MESSAGES[language]
        for status in PublishStatusEnum:
            _ = messages[f"enum.publishStatus.{status.value}"]
        for key in GRADE_MESSAGE_KEYS.values():
            _ = messages[key]
        for role in RoleEnum:
            _ = messages[f"enum.role.{role.value}"]
        for reaction in NoteReactionKind:
            _ = messages[f"enum.noteReaction.{reaction.value}"]
        for source in NoteViewSourceCategory:
            _ = messages[f"enum.noteViewSource.{source.value}"]


validate_i18n_catalog()
